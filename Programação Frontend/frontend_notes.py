# 1. UTILIZANDO CLASSES NO DJANGO (CLASS-BASED VIEWS)
"""
Vantagens das views baseadas em classe:
- Reutilização de código através de hierarquia de classes
- Facilidade de manutenção com código mais organizado e modular
- Flexibilidade para lidar com diferentes requisições HTTP
- Herança de classes para criar views genéricas e especializadas
- Maior testabilidade com lógica isolada em classes individuais

Exemplos de classes disponíveis no Django:
- View: classe base
- TemplateView: para renderização de templates
- ListView: para exibição de listas de objetos
- DetailView: para exibição de detalhes de um objeto
"""

# 2. HTML5 E CSS NO PROJETO
"""
Fundamentos importantes:
- HTML5: novas tags semânticas (header, nav, section, article, footer)
- Estilização com CSS: controle de cores, tipografia, espaçamento, etc.
- Seletores CSS: elemento, classe, ID, atributo, combinados
- Box Model: conteúdo, padding, border, margin
- Layouts Flexíveis: flexbox e grid
- Media Queries: para design responsivo
- Frameworks CSS: Bootstrap, Foundation, Bulma
- Animações e Transições: para adicionar interatividade
- Responsividade: adaptação a diferentes dispositivos
"""

